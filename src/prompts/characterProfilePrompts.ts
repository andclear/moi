const CHARACTER_PROFILE_PROMPT = CHARACTER_PROFILE_PROMPT_LINES.join("\n");

export function buildCharacterProfileYamlMessages(
  characterProfile: string,
  previousCharacterInfo = "",
): LlmMessage[] {
  return [
    {
      role: "user",
      content: [
        CHARACTER_PROFILE_PROMPT.replace("{{character_profile}}", characterProfile),
        "",
        "当前已有的角色信息 YAML：",
        previousCharacterInfo || "尚未生成",
        "",
        "重要保持规则：如果“当前已有的角色信息 YAML”中已经存在 姓名 字段，新的 YAML 必须原样保留这个姓名，不得改名、换名、扩写、翻译或重新取名。用户手动保存过的姓名是最高优先级事实。",
        "如果已有 YAML 中存在年龄、性别、别名等用户明确保存过的基础字段，也必须优先保留，除非当前角色档案明确给出用户更新后的同一字段。",
      ].join("\n"),
    },
  ];
}

export function buildCharacterProfileTextRewriteMessages(input: {
  dossierMarkdown: string;
  characterInfo: string;
  selectedFragment: string;
  revisionNotes: string;
}): LlmMessage[] {
  return [
    {
      role: "system",
      content: [
        "你是虚拟角色资料整理助手。你的任务是按照用户要求，直接改写一小段角色档案内容。",
        "必须使用简体中文，只输出修改后的内容，不要解释，不要总结，不要输出 Markdown 代码块。",
        "保持原有语气、视角和档案风格，不要把这段文字扩写成新的段落结构。",
        "如果用户要求的是局部替换，就只返回那一段应该被替换成的新文本。",
      ].join("\n"),
    },
    {
      role: "user",
      content: [
        "当前的角色档案是：{{charater_profile}}",
        "角色信息是：{{character_info}}",
        "当前用户需要修改的文本内容是：{{text_to_edit}}",
        "用户的修改意见是：{{revision_notes}}",
        "请参考角色档案、角色信息中的内容，依据用户的修改要求，直接修改这段文字。",
        "最终只输出修改后的内容即可。",
        "",
        `{{charater_profile}} = ${input.dossierMarkdown}`,
        `{{character_info}} = ${input.characterInfo}`,
        `{{charater_info}} = ${input.characterInfo}`,
        `{{text_to_edit}} = ${input.selectedFragment}`,
        `{{revision_notes}} = ${input.revisionNotes}`,
      ].join("\n"),
    },
  ];
}
